
// how include statments works?
// # include menas "copy- paste"
// before compilation stats, pre-processor copy past the file content
// instead of the  # include xxxx line.
// For example: pre-porcessor will copy and paste the content of "heehaw.hoohoo" file
// to instead of line ' #include "heehaw.hoohoo"'.
// Then it  will copy and paste the content of "hoohoo.txt" file
// to instead of line '#include "hoohoo.txt"'.
// after that compiler will start to compile our code.
#include "heehaw.hoohoo"
#include "hoohoo.txt"
